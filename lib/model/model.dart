class ToDo {
  String userId;
  String title;
  String description;
  String exerciseDuration;
  double runningDistance;
  DateTime startTime;
  DateTime finishTime;

  ToDo({
    required this.userId,
    required this.title,
    required this.description,
    required this.exerciseDuration,
    required this.runningDistance,
    required this.startTime,
    required this.finishTime,
  });
}